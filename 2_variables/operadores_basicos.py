# Un operador nos permite realizar operaciones con nuestra variables
# Tipos de operadores básico:
# 1 - Ariméticos: Suma, Resta, Multiplicacion.
# 2 - Logico
# 3 - De Asignacion

x = 10
y = 3

print('El valor de x es:', x)
print('El valor de y es:', y)

# suma
print('Suma:', x + y)

# Resta
print('Resta:', x - y)

# Multiplicacion
print('Multiplicacion:', x * y)

# Division
print('Division:', x / y)

# Division Redondeada
print('Division Redondeada:', x // y)

# Modulo
print('Modulo:', x % y)

# Exponente
print('Exponente:', x ** y)


# ----- operaradores de a asignacion avanzados.

# x = x + 2

print('x =', x)
x = x + 2
print('Nuevo Valor:', x)

# Es el mismo resultado.
x = 10
x += 2
print('El valor x:', x)