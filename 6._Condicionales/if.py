# Comprobar si dos elementos son iguales
print("Es 5 == 5:", 5 == 5)
print("Es patata === cebolla", "patata == cebolla")

# Comprobar si dos elementos son diferentes
print("Es 5 != 5:", 5 != 5)
print("Es patata === cebolla", "patata != cebolla")

# Comprobaciones aritmeticas: Mayor que, Mayor o igual que, Menor o igual que
print("Es 5 > 5:", 5 > 2)
print("Es 5 > 5:", 5 >= 10)
print("Es 5 > 5:", 5 < 2)
print("Es 5 > 5:", 5 <= 10)
